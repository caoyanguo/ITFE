package com.cfcc.model.data;

public class WidgetData {
	private String text;
	private String label;
	private String compoent;
	private String colename;
	private String colcname;
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getCompoent() {
		return compoent;
	}
	public void setCompoent(String compoent) {
		this.compoent = compoent;
	}
	public String getColename() {
		return colename;
	}
	public void setColename(String colename) {
		this.colename = colename;
	}
	public String getColcname() {
		return colcname;
	}
	public void setColcname(String colcname) {
		this.colcname = colcname;
	}
}
